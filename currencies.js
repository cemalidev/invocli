// Validate if currency code is supported
export function isCurrencySupported(currencyCode) {
  return currencies.hasOwnProperty(currencyCode.toUpperCase());
}

// Format currency amount according to currency rules
export function formatCurrency(amount, currencyCode) {
  const currency = currencies[currencyCode.toUpperCase()];
  if (!currency) {
    // Fallback for unsupported currencies
    return `${amount.toFixed(2)} ${currencyCode}`;
  }

  const { symbol, thousandsSeparator, decimalSeparator, symbolOnLeft, spaceBetweenAmountAndSymbol, decimalDigits } = currency;
  
  // Format the number with proper decimal places
  const fixedAmount = amount.toFixed(decimalDigits);
  const [integerPart, decimalPart] = fixedAmount.split('.');
  
  // Add thousands separator
  let formattedInteger = integerPart;
  if (thousandsSeparator) {
    formattedInteger = integerPart.replace(/\B(?=(\d{3})+(?!\d))/g, thousandsSeparator);
  }
  
  // Combine integer and decimal parts
  let formattedAmount = formattedInteger;
  if (decimalDigits > 0 && decimalPart) {
    formattedAmount += decimalSeparator + decimalPart;
  }
  
  // Add currency symbol
  const space = spaceBetweenAmountAndSymbol ? ' ' : '';
  if (symbolOnLeft) {
    return `${symbol}${space}${formattedAmount}`;
  } else {
    return `${formattedAmount}${space}${symbol}`;
  }
}
